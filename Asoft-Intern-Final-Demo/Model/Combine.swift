class Combine {
    
    var id: String
    var image: String
    var type: String
    var detailName: String
    var name: String
    
    init(id: String, image: String, type: String, detailName: String, name: String) {
        self.id = id
        self.image = image
        self.type = type
        self.detailName = detailName
        self.name = name
    }
    
}
